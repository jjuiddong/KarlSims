
#include "StdAfx.h"
#include "sphere.h"


//-----------------------------------------------------------------------------//
// Sphere 초기화
//-----------------------------------------------------------------------------//
void Sphere::SetSphere( float r, Vector3 center )
{
	m_R = r;
	m_vCenter = center;
}


//-----------------------------------------------------------------------------//
// 충돌 테스트
//-----------------------------------------------------------------------------//
BOOL Sphere::Collision( Sphere *pSphere )
{
	float len = sqrt( m_vCenter.LengthRoughly(pSphere->m_vCenter) );
	return (len < m_R + pSphere->m_R);
}

